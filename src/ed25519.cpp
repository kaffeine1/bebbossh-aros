static void setzero(ed_t *r) {
#if (EDSIZE == 8)
	r[0] = 0;
	r[1] = 0;
	r[2] = 0;
	r[3] = 0;
	r[4] = 0;
	r[5] = 0;
	r[6] = 0;
	r[7] = 0;
#else
	for (short i = 0; i < EDSIZE; i++)
		r[i] = 0;
#endif
}

void ed25519_from32bytes(uint16_t r[32], const uint8_t x[32]) {
	int i;
	uint16_t t[64];
	for (i = 0; i < 32; i++)
		t[i] = x[i];
	for (i = 32; i < 64; ++i)
		t[i] = 0;
	barrett_reduce(r, t);
}

void ed25519_from64bytes(uint16_t r[32], const uint8_t x[64]) {
	uint16_t t[64];
	for (short i = 0; i < 64; ++i)
		t[i] = x[i];
	barrett_reduce(r, t);
}

void ge25519_pack(uint8_t r[32], const ge25519 *p) {
	ed25519 tx, ty, zi;
	recip16(zi, p->z);
	edmul(tx, p->x, zi);
	edmul(ty, p->y, zi);
	pack16(r, ty);
	r[31] ^= (tx[0] & 1) << 7;
}

/* Reduce coefficients of r before calling sc25519_reduce_add_sub */
void ed25519_reduce_add_sub(uint16_t r[32])
{
	uint16_t b;
  int i;
  unsigned char t[32];

  uint16_t pb = 0;
  for(i=0;i<32;i++)
  {
    pb += ed25519_m[i];
    b = r[i] < pb;
    t[i] = 0xff & (r[i]-pb+(b<<8));
    pb = b;
  }
  if (!b)
	  for(i=0;i<32;i++)
		  r[i] = t[i];
}

/* Reduce coefficients of x before calling barrett_reduce */
void barrett_reduce(uint16_t r[32], const uint16_t x[64]) {
	/* See HAC, Alg. 14.42 */
	int i, j;
	uint16_t q2[66];
	uint16_t *q3 = q2 + 33;
	uint16_t r2[33];

	for (i = 0; i < 66; ++i)
		q2[i] = 0;
	for (i = 0; i < 33; ++i)
		r2[i] = 0;

	for (i = 0; i < 33; i++) {
		unsigned u = 0;
		for (j = 31 - i; j < 33; j++) {
			u += q2[i + j];
			u += ed25519_mu[i] * x[j + 31];
			q2[i + j] = u & 0xff;
			u >>= 8;
		}
		q2[i + j] += u;
	}

	for (i = 0; i < 32; i++) {
		unsigned u = 0;
		for (j = 0; j < 33 - i; j++) {
			u += r2[i + j];
			u += ed25519_m[i] * q3[j];
			r2[i + j] = u & 0xff;
			u >>= 8;
		}
		r2[i + j] += u;
	}

	unsigned int pb = 0;
	for (i = 0; i < 32; i++) {
		pb += r2[i];
		r[i] = 0xff & (x[i] - pb);
		pb = x[i] < pb;
	}

	/* XXX: Can it really happen that r<0?, See HAC, Alg 14.42, Step 3
	 * If so: Handle  it here!
	 * SBF: YES!
	 * verify
	 * pk "8060EEB1AFDC77B871A0F3E126E7FCEC642695260BA03BA460E8D0FDA7FD5CC3"
	 * msg "65F21DD076E0A543D965AB2C0D5DC3B70992F69B5D7A1005CE55CE0DD9469F91"
	 * signature "0DFE2C5D9D63FDA7282BC01D5AD49ADA27B4F3CAF0CB5588FE22B52CC3519B455F748CFE80D1944A3F6113640EA9050F4FA2EA5982535F792C9BDC77E2A1600F"
	 * and reduce_add_sub is needed.
	 */

  ed25519_reduce_add_sub(r);
  ed25519_reduce_add_sub(r);
}

static inline void setneutral(ge25519 *r) {
	setzero(r->x);
	setone(r->y);
	setone(r->z);
	setzero(r->t);
}

void add_p1p1(ge25519 *r, const ge25519 *p, const ge25519 *q) {
	ed25519 a, b, c, d, t;

	edsub(b, p->y, p->x); /* A = (Y1-X1)*(Y2-X2) */
	edsub(t, q->y, q->x);
	edmul(a, b, t);
	edadd(c, p->x, p->y); /* B = (Y1+X1)*(Y2+X2) */
	edadd(t, q->x, q->y);
	edmul(b, c, t);
	edmul(d, p->t, q->t); /* C = T1*k*T2 */
	edmul(c, d, ge25519_ec2d);
	edmul(d, p->z, q->z); /* D = Z1*2*Z2 */
	edadd(d, d, d);
	edsub(r->x, b, a); /* E = B-A */
	edsub(r->t, d, c); /* F = D-C */
	edadd(r->z, d, c); /* G = D+C */
	edadd(r->y, b, a); /* H = B+A */
}

/* See http://www.hyperelliptic.org/EFD/g1p/auto-twisted-extended-1.html#doubling-dbl-2008-hwcd */
static void dbl_p1p1(ge25519 *r, const ge25519_p2 *p) {
	ed25519 a, b, c, d;
	edsquare(a, p->x);
	edsquare(b, p->y);
	edsquare(c, p->z);
	edadd(c, c, c);

	edadd(r->x, p->x, p->y);
	edsquare(d, r->x);
	edsub(r->x, d, a);
	edsub(r->x, r->x, b);

	edsub(d, zero, a);
	edadd(r->z, d, b);
	edsub(r->t, r->z, c);
	edsub(r->y, d, b);
}

void p1p1_to_p2(ge25519_p2 *r, const ge25519 *p) {
	edmul(r->x, p->x, p->t);
	edmul(r->y, p->y, p->z);
	edmul(r->z, p->z, p->t);
}

static void p1p1_to_p3(ge25519 *r, const ge25519 *p) {
	p1p1_to_p2((ge25519_p2*) r, p);
	edmul(r->t, p->x, p->y);
}

void ge25519_scalarmult_vartime_pre(ge25519 *r, const ge25519 pre[16], const uint16_t s1[32]) {
	ge25519 tp1p1;

	int pos = 254;
	int mask = 0;

	// first mask needs highest bit set
	while (pos > 3 && mask < 8) {
		mask <<= 1;
		mask |= (s1[pos / 8] >> (pos & 7)) & 1;
		--pos;
	}

	*r = pre[mask];

	while (pos >= 0) {
		mask = (s1[pos / 8] >> (pos & 7)) & 1;
		--pos;
		dbl_p1p1(&tp1p1, (ge25519_p2*) r);
		if (mask) {
			int i = 1;
			for (; pos >= 0 && i < 4; ++i) {
				mask <<= 1;
				mask |= (s1[pos / 8] >> (pos & 7)) & 1;
				--pos;
			}
			while (--i > 0) {
				p1p1_to_p2((ge25519_p2*) r, &tp1p1);
				dbl_p1p1(&tp1p1, (ge25519_p2*) r);
			}
			p1p1_to_p3(r, &tp1p1);
			add_p1p1(&tp1p1, r, &pre[mask]);
		}
		p1p1_to_p2((ge25519_p2*) r, &tp1p1);
	}
	edmul(r->t, tp1p1.x, tp1p1.y);
}

void ge25519_scalarmult_vartime_base(ge25519 *r, const uint16_t s[32]) {
	ge25519_scalarmult_vartime_pre(r, base_pre, s);
}

void precalc(ge25519 pre[16], ge25519 const *p1) {
	ge25519 tp1p1;
	/* precomputation                                                         s2 s1 */
	setneutral(pre); /* 00 00 */
//_dump("pre", pre, sizeof(ge25519));
	pre[1] = *p1; /* 00 01 */
	dbl_p1p1(&tp1p1, (ge25519_p2*) p1);
	p1p1_to_p3(&pre[2], &tp1p1); /* 00 10 */
	add_p1p1(&tp1p1, &pre[1], &pre[2]);
	p1p1_to_p3(&pre[3], &tp1p1); /* 00 11 */
	dbl_p1p1(&tp1p1, (ge25519_p2*) &pre[2]);
	p1p1_to_p3(&pre[4], &tp1p1); /* 01 00 */
	add_p1p1(&tp1p1, &pre[1], &pre[4]);
	p1p1_to_p3(&pre[5], &tp1p1); /* 01 01 */
	dbl_p1p1(&tp1p1, (ge25519_p2*) &pre[3]);
	p1p1_to_p3(&pre[6], &tp1p1); /* 01 10 */
	add_p1p1(&tp1p1, &pre[1], &pre[6]);
	p1p1_to_p3(&pre[7], &tp1p1); /* 01 11 */
	dbl_p1p1(&tp1p1, (ge25519_p2*) &pre[4]);
	p1p1_to_p3(&pre[8], &tp1p1); /* 10 00 */
	add_p1p1(&tp1p1, &pre[1], &pre[8]);
	p1p1_to_p3(&pre[9], &tp1p1); /* 10 01 */
	dbl_p1p1(&tp1p1, (ge25519_p2*) &pre[5]);
	p1p1_to_p3(&pre[10], &tp1p1); /* 10 10 */
	add_p1p1(&tp1p1, &pre[1], &pre[10]);
	p1p1_to_p3(&pre[11], &tp1p1); /* 10 11 */
	dbl_p1p1(&tp1p1, (ge25519_p2*) &pre[6]);
	p1p1_to_p3(&pre[12], &tp1p1); /* 11 00 */
	add_p1p1(&tp1p1, &pre[1], &pre[12]);
	p1p1_to_p3(&pre[13], &tp1p1); /* 11 01 */
	dbl_p1p1(&tp1p1, (ge25519_p2*) &pre[7]);
	p1p1_to_p3(&pre[14], &tp1p1); /* 11 10 */
	add_p1p1(&tp1p1, &pre[1], &pre[14]);
	p1p1_to_p3(&pre[15], &tp1p1); /* 11 11 */
}

void ge25519_scalarmult_vartime_pre2(ge25519 *r, const ge25519 pre1[16], const uint16_t s1[32],
		const ge25519 pre2[16], const uint16_t s2[32]) {
	ge25519 tp1p1;

	int pos = 254;
	int mask1 = 0, mask2 = 0;

	// first mask needs highest bit set
	while (pos > 3 && mask1 < 8 && mask2 < 8) {
		mask1 <<= 1;
		mask1 |= (s1[pos / 8] >> (pos & 7)) & 1;
		mask2 <<= 1;
		mask2 |= (s2[pos / 8] >> (pos & 7)) & 1;
		--pos;
	}

//	_dump("pre1[mask1],", &pre1[mask1], sizeof(ge25519));
//	_dump("pre2[mask2])", &pre2[mask2], sizeof(ge25519));

	add_p1p1(&tp1p1, &pre1[mask1], &pre2[mask2]);
	p1p1_to_p2((ge25519_p2*)r, &tp1p1);

//	_dump("p1p1_to_p2", r, sizeof(ge25519_p2));

	while (pos >= 0) {
		mask1 = (s1[pos / 8] >> (pos & 7)) & 1;
		mask2 = (s2[pos / 8] >> (pos & 7)) & 1;
		--pos;
		dbl_p1p1(&tp1p1, (ge25519_p2*) r);
		if (mask1 | mask2) {
			int i = 1;
			for (; pos >= 0 && i < 4; ++i) {
				mask1 <<= 1;
				mask1 |= (s1[pos / 8] >> (pos & 7)) & 1;
				mask2 <<= 1;
				mask2 |= (s2[pos / 8] >> (pos & 7)) & 1;
				--pos;
			}
			while (--i > 0) {
				p1p1_to_p2((ge25519_p2*) r, &tp1p1);
				dbl_p1p1(&tp1p1, (ge25519_p2*) r);
			}
			if (mask1) {
				p1p1_to_p3(r, &tp1p1);
				add_p1p1(&tp1p1, r, &pre1[mask1]);
			}
			if (mask2) {
				p1p1_to_p3(r, &tp1p1);
				add_p1p1(&tp1p1, r, &pre2[mask2]);
			}
		}
		p1p1_to_p2((ge25519_p2*) r, &tp1p1);
	}
	edmul(r->t, tp1p1.x, tp1p1.y);
}

