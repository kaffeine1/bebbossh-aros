bool testGCM5() {
	puts("testGCM5");
	GCM gcm(new AES());

	uint8_t key[16];
	unhexlify(key, "98 BA CA 58 7C 4E 49 EA  BE 7B 94 21 86 0F 08 65");

	uint8_t encIvWrite[12];
	unhexlify(encIvWrite, "2B 5B E8 5C 0F CB 0F AB  DE 90 41 78");

	uint8_t buffer[68 + 16];
	unhexlify(buffer, "00 00 00 40 2E 05 00 00  00 0C 73 73 68 2D 75 73"
			"65 72 61 75 74 68 C3 80  86 75 68 1F 67 23 0B 7D"
	"55 02 BB FB 07 4B 5B B7  A6 4C 70 87 CA 2C 1D AE"
	"35 8D 17 F4 55 B6 CD BE  CF C1 45 61 1D 25 2F 21"
	"2A DB 4B 4E");

	gcm.setKey(key, 16);
	gcm.init(encIvWrite, 12);
	gcm.updateHash(buffer, 4);
	gcm.encrypt(buffer + 4, buffer + 4, 64);
	gcm.calcHash(buffer + 68);

	uint8_t expected[68+16];
	unhexlify(expected,
	"00 00 00 40 A4 17 07 4A  34 75 54 3E 63 F4 96 57"
	"BE A1 08 7F F4 ED 38 4B  F4 48 B8 C7 F4 2A 9E A3"
	"26 DA 47 22 1A 4F C2 7E  A9 A8 01 24 95 1C E7 B8"
	"84 9A 95 46 FA E8 AC 24  5C 2A 76 56 FB 91 78 3F"
	"AC 12 D9 33 D8 DC 04 9D  0F 86 0B 87 FE 74 85 30"
	"8D 06 99 AA");

	return assertArrayEquals(expected, buffer, 68+16);
}


