bool testGCM1() {
	puts("testGCM1");
	static uint8_t key[16] = { 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00 };
	static uint8_t plainText[1] = { 0 };
	static uint8_t aadText[1] = { 0 };
	static uint8_t nonce[12] = { 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00 };
	static uint8_t cipherExpected[1] = { 0 };
	static uint8_t hashExpected[16] = { 0x58, 0xe2, 0xfc, 0xce, 0xfa, 0x7e, 0x30, 0x61, 0x36, 0x7f, 0x1d, 0x57, 0xa4, 0xe7, 0x45, 0x5a };

	uint8_t encrypted[1];
	uint8_t hash[16];


	GCM gcm(new AES());
	gcm.setKey(key, 16);
	gcm.init(nonce, 12);
	gcm.updateHash(aadText, 0);

	gcm.encrypt(encrypted, plainText, 0);
	gcm.calcHash(hash);

	return assertArrayEquals(cipherExpected, encrypted, 0) && assertArrayEquals(hashExpected, hash, 16);
}

